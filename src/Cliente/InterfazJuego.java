    /**
     * Metodo que comprueba si la letra pertenece o no a la palabra
     * @param letra
     * @throws HeadlessException 
     */
    public void comportamientoBoton(String letra) throws HeadlessException {
        if (!introducirLetra(letra)) {
            errores++;
            cambiarLabel();
            JOptionPane.showMessageDialog(this, "Has fallado", "Fallo", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Letra introducida", "Acierto", JOptionPane.INFORMATION_MESSAGE);
        }
        rondas++;
        labelErrores.setText(String.valueOf(errores));
        labelRondas.setText(String.valueOf(rondas));
        Socket miJugador;
        DataOutputStream output;
        DataInputStream input;
        try {
            miJugador = new Socket(host, puerto);
            output = new DataOutputStream(miJugador.getOutputStream());
            input = new DataInputStream(miJugador.getInputStream());
            output.writeInt(1);
            output.writeUTF(nombre);
            output.writeInt(rondas);
            output.writeInt(errores);
            for (int i = 0; i < palabra.length(); i++) {
                if (seleccionarLabel(i).getText().contentEquals("")) {
                    setAcabado(false);
                    break;
                } else {
                    setAcabado(true);
                }
            }
            if (isAcabado() || isPerdido()) {
                //InterfazEsperar ie = new InterfazEsperar();
                output.writeBoolean(true);
                setEnabled(false);
                input.readInt();
                if (input.readUTF().contentEquals(nombre)) {
                    //ie.dispose();
                    dispose();
                    InterfazGanador ig = new InterfazGanador();
                } else {
                    //ie.dispose();
                    dispose();
                    InterfazPerdido ip = new InterfazPerdido();
                }
            }

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No puedes conectarte al servidor");
        }
    }

    /**
     * Metodo que devuelve true si ha metido la letra en un label
     * @param letra
     * @return 
     */
    public boolean introducirLetra(String letra) {
        boolean introduce = false;
        for (int i = 0; i < palabra.length(); i++) {
            if (palabra.charAt(i) == letra.charAt(0)) {
                seleccionarLabel(i).setText(String.valueOf(letra));
                introduce = true;
            }
        }
        return introduce;
    }

