    /**
     * Metodo main de la interfaz
     * @param args
     * @throws ClassNotFoundException
     * @throws InterruptedException 
     */
    public static void main(String[] args) throws ClassNotFoundException, InterruptedException {
        try {
            Socket miJugador = new Socket(host, puerto);
            DataOutputStream output = new DataOutputStream(miJugador.getOutputStream());
            DataInputStream input = new DataInputStream(miJugador.getInputStream());
            InterfazNombre in = new InterfazNombre();
            while (!in.isPulsado()) {
                System.out.println("");
            }
            output.writeInt(0);
            if (input.readBoolean()) {
                JOptionPane.showMessageDialog(null, "La partida ya ha comenzado");
            } else {
                output.writeUTF(in.getjTextField1().getText());
                String palabra = input.readUTF();
                InterfazJuego ij = new InterfazJuego(palabra, in.getjTextField1().getText());
                ij.setVisible(true);
                ij.setEnabled(false);
                input.readInt();
                ij.setEnabled(true);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No puedes conectarte al servidor");
        }
    }
